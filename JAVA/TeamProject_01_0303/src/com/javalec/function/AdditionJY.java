package com.javalec.function;

public class AdditionJY {
	
	// Field
	public int num1 ;
	public int num2 ;
	
	//Constructor
	public AdditionJY()	{
	}
	
	//Method
	public void addition(int num1, int num2) {
		System.out.println(num1+ " + " + num2 + " = "+ ( num1 + num2 ));
	}
	
	public void substraction(int num1,int num2)	{
		System.out.println(num1+ " - " + num2 + " = " + ( num1 - num2 ));
	}
}
